import { build } from 'npm:vite@^8.1.5';
import { fileURLToPath } from 'node:url';
import { walk } from 'jsr:@std/fs';
import { relative } from 'jsr:@std/path';

const dashRoot = fileURLToPath(new URL('../../dashboard', import.meta.url));
const outDir = fileURLToPath(new URL('../../dist-dashboard', import.meta.url));

const TYPES: Record<string, string> = {
  '.html': 'text/html; charset=utf-8',
  '.js': 'text/javascript; charset=utf-8',
  '.css': 'text/css; charset=utf-8',
};

export async function buildDashboard(): Promise<void> {
  await build({
    root: dashRoot,
    logLevel: 'warn',
    build: { outDir, emptyOutDir: true, minify: false },
  });

  const assets: Record<string, { body: string; type: string }> = {};
  for await (const entry of walk(outDir, { includeDirs: false })) {
    const rel = '/' + relative(outDir, entry.path).replaceAll('\\', '/');
    const ext = rel.slice(rel.lastIndexOf('.'));
    const key = rel === '/index.html' ? '/' : rel;
    assets[key] = { body: await Deno.readTextFile(entry.path), type: TYPES[ext] ?? 'application/octet-stream' };
  }

  const gen = `// AUTO-GENERATED by build-dashboard.ts — do not edit.\n` +
    `import type { DashboardAssets } from './dashboard-assets.d.ts';\n` +
    `export const DASHBOARD_ASSETS: DashboardAssets = ${JSON.stringify(assets, null, 0)};\n`;
  await Deno.writeTextFile(fileURLToPath(new URL('../generated/dashboard-assets.ts', import.meta.url)), gen);
  console.log(`built dashboard (${Object.keys(assets).length} assets)`);
}

if (import.meta.main) await buildDashboard();
